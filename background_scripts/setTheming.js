const onSuccess = () => {
  console.log("item(s) saved to storage")
}

const onError = (error) => {
  console.log(error)
}

const initTheme = () => {
  browser.storage.local.set({savedTheme:  {name:"figma-dark"}})
  browser.storage.local.set({figmaDark, figmaLight, figjamFlat, figjamPop})
  .then(onSuccess, onError)

  browser.theme.update(figmaDark.theme)
}

